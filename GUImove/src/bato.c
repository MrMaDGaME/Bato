gboolean on_draw(GtkWidget *widget, cairo_t *cr, gpointer user_data)
{
  // Gets the 'Game' structure.
  Game *game = user_data;

  // Sets the background to white.
  cairo_set_source_rgb(cr, 0, 0.33, 0.5);
  cairo_paint(cr);


  draw_arena(widget, cr, user_data);

/*
  float RGB[BOAT_RECTS * 3] = {0.33, 0.2, 0,     0.33, 0.2, 0,     0.33, 0.2, 0,     
                               0.49, 0.3, 0,     0.33, 0.2, 0,     1, 1, 1,     
                               1, 1, 1,     0.33, 0.2, 0,     1, 1, 1,     1, 1, 1,     0.33, 0.2, 0,     0.5,0.5,0.5,     0.5,0.5,0.5};
*/

  cairo_set_source_rgb(cr, 0.33, 0.2, 0);
  cairo_move_to (cr, game->p.rect.x, game->p.rect.y);
  cairo_rel_line_to (cr, cosf(game->p.dir) * game->p.rect.width, sinf(game->p.dir) * game->p.rect.width);
  cairo_rel_line_to (cr, -sinf(game->p.dir) * game->p.rect.height, cosf(game->p.dir) * game->p.rect.height);
  cairo_rel_line_to (cr, -cosf(game->p.dir) * game->p.rect.width, -sinf(game->p.dir) * game->p.rect.width);
  cairo_close_path (cr);
  cairo_fill(cr);

  GdkPixbuf *pix;
  GError *err = NULL;
  /* Create pixbuf */
  pix = gdk_pixbuf_new_from_file("../texture/boat/pixel_player_boat.png", &err);
  if(err){
    printf("Error : %s\n", err->message);
    g_error_free(err);
    return FALSE;
  }

  gdk_cairo_set_source_pixbuf(cr, pix, game->p.rect.x - game->p.rect.width/3, game->p.rect.y - game->p.rect.height/2);
  cairo_paint(cr);
  cairo_rotate(cr, PI/2);
  cairo_paint(cr);

  return FALSE;

}


void redraw_item(GtkDrawingArea *area, GdkRectangle *old, GdkRectangle *new)
{
  gdk_rectangle_union(old, new, old);

  gtk_widget_queue_draw_area(GTK_WIDGET(area),
			     (*old).x-((*old).width*2), (*old).y-((*old).width*2), (*old).width*4,(*old).width*4);
}

gboolean player_move(gpointer user_data){
    Game *game = user_data;

    GdkRectangle old = game->p.rect;
            
    game->p.rect.y = game->p.rect.y + (game->p.speed * sinf(game->p.dir));
    game->p.rect.x = game->p.rect.x + (game->p.speed * cosf(game->p.dir));


    redraw_item(game->ui.area, &old, &game->p.rect);

    return TRUE;
}

gboolean speed_to_sail(gpointer user_data){
    Game *game = user_data;

    if(game->p.speed < game->p.sail){
        game->p.speed += 0.05;
    }
    if(game->p.speed >= 0.02 && game->p.speed > game->p.sail){
        game->p.speed -= 0.03;
    }

    return TRUE;
}

gboolean progress_bar(gpointer user_data){
    Game *game = user_data;

    gtk_progress_bar_set_fraction(GTK_PROGRESS_BAR(game->ui.lifebar), (game->p.hp)/100);
    gtk_progress_bar_set_fraction(GTK_PROGRESS_BAR(game->ui.sailbar), (game->p.sail)/MAX_SAIL);

    return TRUE;
}

gboolean on_key_press(GtkWidget *widget, GdkEventKey *event, gpointer user_data){
    Game *game = user_data;
    gboolean b = FALSE;

    if(event->keyval == GDK_KEY_d){
        game->p.dir += 0.025;
        b = TRUE;
    }

    if(event->keyval == GDK_KEY_q){
        game->p.dir -= 0.025;
        b = TRUE;
    }

    if(event->keyval == GDK_KEY_z){
        if(game->p.sail < MAX_SAIL){
            game->p.sail += 0.5;
        }
        
        b = TRUE;
    }

    if(event->keyval == GDK_KEY_s){
        if(game->p.sail >= 0.05){
            game->p.sail -= 0.05;
        }
        
        b = TRUE;
    }
    if(game->p.sail < 0.05){
        game->p.sail = 0;
    }
    if(game->p.speed < 0.05){
        game->p.speed = 0;
    }

    printf("Vitesse: %f\n", game->p.speed);

    return b;
}

int main(int argc, char *argv[]){
    gtk_init(NULL,NULL);

    GtkBuilder *builder = gtk_builder_new();

    GError* error = NULL;
    if(gtk_builder_add_from_file(builder, "bato.glade", &error) == 0){
        g_printerr("Error loading file: %s\n", error->message);
        g_clear_error(&error);
        return 1;
    }

    GtkWindow* window = GTK_WINDOW(gtk_builder_get_object(builder, "org.gtk.bato"));
    GtkDrawingArea* area = GTK_DRAWING_AREA(gtk_builder_get_object(builder, "area"));
    GtkProgressBar *lifebar = GTK_PROGRESS_BAR(gtk_builder_get_object(builder, "lifebar"));
    GtkProgressBar *sailbar = GTK_PROGRESS_BAR(gtk_builder_get_object(builder, "sailbar"));

    Game game =
    {
        .p =
        {
            .rect = {BOAT_INIT_X, BOAT_INIT_Y, BOAT_WIDTH, BOAT_HEIGHT},
            .dir = 0,
            .hp = 100,
            .speed = 0,
            .sail = 0,
            .event = 0,
        },

        .ui =
        {
            .window = window,
            .area = area,
            .lifebar = lifebar,
            .sailbar = sailbar,
        },
    };

    
    g_signal_connect(window, "destroy", G_CALLBACK(gtk_main_quit), NULL);
    g_signal_connect(area, "draw", G_CALLBACK(on_draw), &game);
    g_signal_connect(window, "key_press_event", G_CALLBACK(on_key_press), &game);

    g_timeout_add(100, speed_to_sail, &game);
    g_timeout_add(100, progress_bar, &game);

    game.p.event = g_timeout_add(100, player_move, &game);
    
    gtk_main();
    return 0;
}
