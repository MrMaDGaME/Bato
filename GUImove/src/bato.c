    for(int i = 1; i < h-1; i++){
        for(int j = 1; j < w-1; j++){
            if(map[i*w+j] == 1 && (map[(i+1)*w+j] == 0 || map[(i-1)*w+j] == 0 || map[i*w+(j+1)] == 0 || map[i*w+(j-1)] == 0)){
                map[i*w+j] = 2;
            }
        }
    }

